public class MyWord {
    private int id;
    private String eng;
    private String rus;
    private int answerTrue;
    private int answerFalse;
    private int level;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getEng() {
        return eng;
    }
    public void setEng(String eng) {
        this.eng = eng;
    }
    public String getRus() {
        return rus;
    }
    public void setRus(String rus) {
        this.rus = rus;
    }
    public int getAnswerTrue() {
        return answerTrue;
    }
    public void setAnswerTrue(int answerTrue) {
        this.answerTrue = answerTrue;
    }
    public int getAnswerFalse() {
        return answerFalse;
    }
    public void setAnswerFalse(int answerFalse) {
        this.answerFalse = answerFalse;
    }
    public int getLevel() {
        return level;
    }
    public void setLevel(int level) {
        this.level = level;
    }




}
